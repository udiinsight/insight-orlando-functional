  <script>
(function() {
    function initWhenReady() {
        if (typeof jQuery === 'undefined') {
            setTimeout(initWhenReady, 100);
            return;
        }
        
        jQuery(document).ready(function($) {
            function initCitySelect2() {
                if (typeof $.fn.select2 === 'undefined') {
                    setTimeout(initCitySelect2, 100);
                    return;
                }
                
                $('#billing_city, #shipping_city').each(function() {
                    if (!$(this).hasClass('select2-hidden-accessible')) {
                        $(this).select2({
                            placeholder: 'הקלד לחיפוש עיר...',
                            allowClear: true,
                            width: '100%',
                            dir: 'rtl',
                            tags: true,
                            createTag: function(params) {
                                var term = $.trim(params.term);
                                if (term === '') {
                                    return null;
                                }
                                return {
                                    id: term,
                                    text: term,
                                    newTag: true
                                };
                            },
                            language: {
                                noResults: function() {
                                    return 'לא נמצאו תוצאות - הקלד והקש Enter להוספה';
                                }
                            }
                        });
                    }
                });
                
                // Auto-focus search field when dropdown opens
                $(document).on('select2:open', '#billing_city, #shipping_city', function() {
                    setTimeout(function() {
                        var searchField = document.querySelector('.select2-container--open .select2-search__field');
                        if (searchField) {
                            searchField.focus();
                        }
                    }, 10);
                });
            }
            
            initCitySelect2();
            
            $(document.body).on('updated_checkout', function() {
                initCitySelect2();
            });
        });
    }
    
    initWhenReady();
})();
